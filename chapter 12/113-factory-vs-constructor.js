//Factory & Constructor উভয়ই OOP এর Class এর alternate হিসেবে কাজ করে।
//Java , C++ এর প্রোগ্রামের কাছে constructor pattern টি বেশী useful ও জনপ্রিয়
// আবার অনেকের মতে Factory patter টি বেশি জনপ্রিয়, কেননা Constructor প্যাটার্নে new keyword টি দিতে ভুলে গেলে সিস্টেমটি কাজ করবেনা।
//তবে এজন্য কোন Error ও দিবেনা এজন্য প্রবলেম ডিটেক্ট করতে বেগ পেতে হয়।
//যখন Constructor Pattern নিয়ে কাজ করা হয়, তখন নিজস্ব একটি constructor থাকবে।
//কিন্ত Factory Pattern নিয়ে কাজ করার ক্ষেত্রে Object এর মাধ্যমে একটি object তৈরি করবে। এজন্য এর constructor হিসেবে থাকবে object.
//তবে কোন Pattern ব্যবহার করার জন্য নির্দিষ্ট কোন বাধ্যবাধকতা নেই, উভয়ই কিন্ত সকল প্রয়োজনীয়তা ফুলফিল করে।



