var a = 10;
var b = 20;

if (a > b) {
  console.log(a + " is greater than" + b);
} else if (a < b) {
  console.log(a + " is less than " + b);
} else {
  console.log("They both are same");
}

var n = 3;
if (n === 0) {
  console.log(n + " is zero");
} else if (n % 2 === 0) {
  console.log(n + " is Even Number");
} else {
  console.log(n + " is Odd Number");
}

